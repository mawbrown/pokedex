function selectPokemon () {
    const pokemon = prompt('Please select a pokemon...'); // This returns a string value
    const dexNum = parseInt(pokemon); // Converts the string to a number so that it can be used to search the array indexes
    const picNum = (dexNum) || (kantoPokedex.indexOf(pokemon.toLowerCase()) + 1); // Returns correct index number for images. Either from the number or string

    if (typeof dexNum === 'number' && dexNum > 0 && dexNum <= kantoPokedex.length) { // Makes sure it's a number, that it's within numeric range 
      console.log(dexNum); // For debugging
      document.getElementById("pokeName").innerText = ('You have selected ' + kantoPokedex[dexNum - 1].toUpperCase() + '!\nNumber #' + dexNum); // Tells the user which pokemon it has selected
    } else if ((typeof pokemon === 'string') && (kantoPokedex.includes(pokemon.toLowerCase()) === true)) { // Makes sure it's a string, and makes it lowercase to make search results in array
      console.log(pokemon); // For Debugging
      console.log(kantoPokedex.indexOf(pokemon) + 1); //Makes .indexOf return the correct PokeDex Number
      document.getElementById("pokeName").innerText = ('You have selected ' + pokemon.toUpperCase() + '!\nNumber #' + (kantoPokedex.indexOf(pokemon) + 1)); // Tells the user which pokemon it has selected
    } else {
      alert('Please enter a Pokemon from the Kanto region.');
    }

    let parent = document.getElementById("imagePlace"); // gets the parent element of the image
    let currentPoke = parent.querySelector("img"); // get an image inside the parent element

    if (currentPoke) { // check if there is already an image in the document
      currentPoke.src = pokemonSprites[picNum - 1]; // if there is, replace src attribute with new value
      console.log(pokemonSprites[picNum - 1]); // For debugging
    } 
}